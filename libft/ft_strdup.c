#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*ret;
	int		i;

	i = ft_strlen(s);
	if (i > 0)
		i++;
	ret = malloc(sizeof(char) * i);
	if (!ret)
		return (ret);
	i = 0;
	while (s[i])
	{
		ret[i] = s[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
